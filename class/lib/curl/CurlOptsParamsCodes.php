static function getCurlOptParamCodeById($mixed) {
if (! is_array ( $mixed ))
return self::$_CURLOPT_PARAMS_CODES_ [$mixed];
else {
$result = array ();
foreach ( $mixed as $key => $value )
$result [isset ( self::$_CURLOPT_PARAMS_CODES_ [$key] ) ? self::$_CURLOPT_PARAMS_CODES_ [$key] : $key] = $value;
return $result;
}
}
